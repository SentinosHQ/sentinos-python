def _rego_for_openai_policy(config: LiveE2EConfig) -> str:
    return f"""
package {config.policy_package}

default decision = {{"decision":"ALLOW","reason":"default allow for governed OpenAI traffic"}}

is_target if {{
  input.action == "{config.target_tool}"
}}

prompt_text := lower(concat(" ", [msg.content |
  msg := input.input.request.messages[_]
  is_string(msg.content)
]))

decision = {{"decision":"DENY","reason":"blocked data exfiltration request"}} if {{
  is_target
  contains(prompt_text, "exfiltrate")
}}

decision = {{"decision":"ESCALATE","reason":"human approval required for wire transfer request"}} if {{
  is_target
  contains(prompt_text, "wire transfer")
}}

decision = {{"decision":"SHADOW","reason":"shadow monitor refund workflows"}} if {{
  is_target
  contains(prompt_text, "refund")
  not contains(prompt_text, "wire transfer")
  not contains(prompt_text, "exfiltrate")
}}
""".strip()


def _create_channels(client: SentinosClient, config: LiveE2EConfig) -> list[str]:
    ids: list[str] = []
    seed = config.run_id.replace("-", "")

    channel_specs: list[dict[str, Any]] = [
        {
            "kind": "SLACK",
            "name": f"live-e2e-slack-{seed}",
            "config": {"webhook_url": "https://example.com/hooks/sentinos/live-e2e/slack"},
        },
        {
            "kind": "WEBHOOK",
            "name": f"live-e2e-webhook-{seed}",
            "config": {"url": "https://example.com/hooks/sentinos/live-e2e/webhook", "method": "POST"},
        },
        {
            "kind": "EMAIL",
            "name": f"live-e2e-email-{seed}",
            "config": {
                "provider": "smtp",
                "from": "alerts@sentinos.dev",
                "to": ["soc@sentinos.dev"],
                "host": "smtp.example.com",
                "port": 587,
            },
        },
    ]

    for spec in channel_specs:
        created = client.kernel.create_notification_channel(
            kind=spec["kind"],
            name=spec["name"],
            config=spec["config"],
            enabled=True,
        )
        channel_id = str(created.get("channel_id") or "").strip()
        if channel_id:
            ids.append(channel_id)
            info(f"Created channel {spec['kind']} -> {channel_id}")
    return ids


