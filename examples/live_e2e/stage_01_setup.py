def _is_live_e2e_rule(rule: Any) -> bool:
    metadata = getattr(rule, "metadata", None) or {}
    if isinstance(metadata, dict) and str(metadata.get("suite") or "").strip().lower() == "live_e2e":
        return True
    name = str(getattr(rule, "name", "")).strip().lower()
    return name.startswith("live e2e ")


def _cleanup_previous_live_e2e_resources(client: SentinosClient, config: LiveE2EConfig) -> None:
    info("Cleaning up previous live-e2e alert rules and channels")

    deleted_rules = 0
    for rule in client.alerts.list_rules(limit=500):
        if not _is_live_e2e_rule(rule):
            continue
        rule_id = str(getattr(rule, "rule_id", "")).strip()
        if not rule_id:
            continue
        try:
            client.alerts.delete_rule(rule_id)
            deleted_rules += 1
        except Exception as exc:  # noqa: BLE001
            info(f"WARN: failed to delete prior live-e2e rule {rule_id}: {exc}")

    deleted_channels = 0
    for channel in client.kernel.list_notification_channels(limit=500):
        name = str((channel or {}).get("name") or "").strip().lower()
        if not name.startswith("live-e2e-"):
            continue
        channel_id = str((channel or {}).get("channel_id") or "").strip()
        if not channel_id:
            continue
        try:
            client.kernel.delete_notification_channel(channel_id)
            deleted_channels += 1
        except Exception as exc:  # noqa: BLE001
            info(f"WARN: failed to delete prior live-e2e channel {channel_id}: {exc}")

    info(f"Cleanup complete: deleted_rules={deleted_rules} deleted_channels={deleted_channels}")


def run_stage(config: LiveE2EConfig) -> dict[str, Any]:
    client = build_sentinos_client(config)
    tenant_id = (config.tenant_id or client.config.tenant_id or "").strip()
    if not tenant_id:
        raise RuntimeError("tenant_id is required for stage_01_setup")
    state = load_state(config)
    rego = _rego_for_openai_policy(config)
    _cleanup_previous_live_e2e_resources(client, config)

    info(f"Upserting policy {config.policy_id}@{config.policy_version}")
    client.arbiter.create_policy(
        policy_id=config.policy_id,
        rego=rego,
        version=config.policy_version,
        target_tools=[config.target_tool],
        tenants=[tenant_id],
        status="draft",
    )
    client.arbiter.promote_policy(
        policy_id=config.policy_id,
        version=config.policy_version,
        target="prod",
    )

    simulation = client.arbiter.simulate_policy(
        candidate_rego=rego,
        trace_limit=200,
        policy_id=config.policy_id,
        version=config.policy_version,
    )
    channel_ids = _create_channels(client, config)
    rule_ids = _create_rules(client, config, channel_ids)

    state.update(
        {
            "run_id": config.run_id,
            "tenant_id": tenant_id,
            "policy_id": config.policy_id,
            "policy_version": config.policy_version,
            "target_tool": config.target_tool,
            "channel_ids": channel_ids,
            "rule_ids": rule_ids,
        }
    )
    save_state(config, state)
    artifact = {
        "stage": "stage_01_setup",
        "run_id": config.run_id,
        "tenant_id": tenant_id,
        "policy_id": config.policy_id,
        "policy_version": config.policy_version,
        "target_tool": config.target_tool,
        "channel_ids": channel_ids,
        "rule_ids": rule_ids,
        "simulation": simulation.to_dict(),
    }
    artifact_path = write_artifact(config, "stage_01_setup", artifact)
    info(f"Stage 01 complete -> {artifact_path}")
    return artifact


def main() -> None:
    config = load_config(require_openai_key=False)
    run_stage(config)


if __name__ == "__main__":
    main()
